<script>
wrapper = $('.tabs');
tabs = wrapper.find('.tab');
tabToggle = wrapper.find('.tab-toggle');
function openTab() {
    var content = $(this).parent().next('.content');
    var activeItems = wrapper.find('.active');
    if (!$(this).hasClass('active')) {
        $(this).add(content).add(activeItems).toggleClass('active');
        wrapper.css('min-height', 370);
        content.find('.checklist-select li').first().click();
    }

};
   
tabToggle.on('click', openTab);

$(window).load(function () {
    tabToggle.first().trigger('click');
});

  
</script>
<script>
$(document).ready(function() {    
    vert_tabs();
    });
    
    $(window).resize(function(){
        if($('.vertical-tabs').innerWidth() > 608) {
            if($('div.selected').length){
            }else{
                $('div.box:first').addClass('selected');
            }
        }
    });

function vert_tabs(){
    $('ul.checklist-select li').click(function() {
        var selectID = $(this).attr('id');
        $('ul.checklist-select li').removeClass('active');
        $(this).addClass('active');
        $('div.box').removeClass('selected');

        $('.' + selectID + '-box').addClass('selected');

        $('div.box.selected')
          .css("transition","all 600 ease")
          .delay(10).queue(function() {
            $(this).css("transform",'scale(0.8)')
            .dequeue();
          }) 
          .delay(200).queue(function() {
            $(this).css("transform",'scale(1)')
            .dequeue();
          }); 

    });    
}

</script>


<script>
